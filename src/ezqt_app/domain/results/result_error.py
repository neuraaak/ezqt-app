"""Pure error model used by typed result objects."""

from __future__ import annotations

# ///////////////////////////////////////////////////////////////
# IMPORTS
# ///////////////////////////////////////////////////////////////
from dataclasses import dataclass

from ...shared.types import JsonMap


# ///////////////////////////////////////////////////////////////
# DATACLASSES
# ///////////////////////////////////////////////////////////////
@dataclass(slots=True)
class ResultError:
    """Structured error payload for operation results."""

    code: str
    message: str
    context: JsonMap | None = None
